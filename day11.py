# Strings: In python anything that enclose between single or double quotation marks is considered a string.
# It is like an array of character.

name = "Srashti"
print("Hello", name)

# To give double quotes inside the string we can use two methods.
print("He said, \"I want to eat an apple\"")
print('He said, "I want to eat an apple"')

# Multiline strings

conversation = """Hi there!
Good morning.
I am looking for a job.
Here is my resume"""

print(conversation)

